#include <stdio.h>

int main() {
  int a = 10;
  int b = 20;

  // Equal to (==)
  printf("a == b: %d\n", a == b);

  // Not equal to (!=)
  printf("a != b: %d\n", a != b);

  // Greater than (>)
  printf("a > b: %d\n", a > b);

  // Less than (<)
  printf("a < b: %d\n", a < b);

  // Greater than or equal to (>=)
  printf("a >= b: %d\n", a >= b);

  // Less than or equal to (<=)
  printf("a <= b: %d\n", a <= b);

  return 0;
}